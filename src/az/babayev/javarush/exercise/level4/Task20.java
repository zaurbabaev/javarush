package az.babayev.javarush.exercise.level4;

/*
Вы пытаетесь найти идеальный пароль, состоящий из двух цифр. Идеальным считается тот, сумма цифр которого равна 13.
Напишите программу, которая ищет первую пару целых чисел i и j (от 1 до 10), сумма которых равна 13. Используйте вложенные циклы for.
Как только пара найдена, выведите на экран "Пароль найден: i=..., j=..." (подставьте найденные значения)
и сразу же завершите оба цикла, чтобы не продолжать поиск.
*/
public class Task20 {
    static void main() {
        for (int i = 1; i <= 10; i++) {
            for (int j = 1; j <= 10; j++) {
                if (i + j == 13) {
                    System.out.println("Пароль найден: i=" + i + ", j=" + j);
                    return;
                }
            }
        }
    }
}
